    private void btnOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOrderActionPerformed
        
        //To sort string
        Arrays.sort(space, (a, b) -> {
            if (a == null && b == null) {
                return 0;
            } else if (a == null) {
                return 1;
            } else if (b == null) {
                return -1;
            } else {
                String pa = a.getSpace();
                String pb = b.getSpace();

                return pa.compareTo(pb) < 0 ? -1 : pa.compareTo(pb) > 0 ? 1: 0;
            }
        });
        
        showSpaceInGeneral();
        
    }//GEN-LAST:event_btnOrderActionPerformed

