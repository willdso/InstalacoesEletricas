    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        int i = 0;
        int i1 = 0;
        while(i1 == 0){
            if(space[i]==null){
                getInputSpaceData(i);
                i1 = 1;
            }
            i++;
        }
        
        //Add the objects space to the list.
        showSpaceInGeneral();
        
        txtSpace.setText("");
        cBType.setSelectedItem("Item 1");
        spnArea.setValue(0);
        spnPerimeter.setValue(0);
    }//GEN-LAST:event_btnAddActionPerformed

