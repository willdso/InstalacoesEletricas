    //Function to change a label to bold with a easy way.
    private void boldPlainName(JLabel lbl, boolean choise){
        Font font = lbl.getFont();
        Font fontModify;
        
        //Get the name once the first time.
        if(getFont == false){
            fontName = font.getFontName();
            getFont = true;
        }
        
        //Change only the style
        if(choise == true){
            fontModify = new Font(fontName,Font.BOLD, font.getSize());
        }
        else{
            fontModify = new Font(fontName,Font.PLAIN, font.getSize());
        }
        
        lbl.setFont(fontModify);
    }
    
    
    //### SPACE IN GENERAL TAB ###
    private void showSpaceInGeneral(){
        listSpaceSupport.removeAllElements();
        for (int i=0;i<space.length;i++) {
            if(space[i]!=null)
                listSpaceSupport.addElement(space[i].getSpace());
        }
    }
    
    private void getInputSpaceData(int i){
        String spInput = txtSpace.getText();
        String tyInput = cBType.getSelectedItem().toString();
        float areaInput = Float.parseFloat(spnArea.getValue().toString());
        float perimeterInput = Float.parseFloat(spnPerimeter.getValue().toString());

        space[i] = new Space(spInput, tyInput, areaInput, perimeterInput);
    }

